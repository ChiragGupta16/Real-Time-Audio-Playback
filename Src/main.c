void Process_PDM_Block(uint8_t* pIn, int16_t* pOut)
{
    
    PDM_Filter(pIn, PCM_Mono, &PDM1_filter_handler); 

    for (int i = 0; i < PCM_OUT_SIZE; ++i) {
        pOut[i*2] = PCM_Mono[i];     // Left
        pOut[i*2 + 1] = PCM_Mono[i]; // Right
    }
}


